package Cards;


public class InfectionCard {


    String name;
    String color;

    public InfectionCard(String name, String color){

        this.name = name;
        this.color = color;

    }

    /**
     * Getters
     */
    public String getName() {
        return name;
    }

    public String getColor() {
        return color;
    }

}
