package Cards;

public class RoleCard{

    String name;


    /**
     * Sets the argument to class variable
     * @param name Argument from initiation
     */
    public RoleCard(String name) {


        this.name = name;

    }

    /**
     * Returns the name of the role
     * @return a string
     */
    public String getName() {
        return name;
    }
}
